package com.zy.dmop.api.common.exception.permission;

public class UnbindPermissionGroupException extends RuntimeException {

	public UnbindPermissionGroupException() {
		super();
	}

	public UnbindPermissionGroupException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UnbindPermissionGroupException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnbindPermissionGroupException(String message) {
		super(message);
	}

	public UnbindPermissionGroupException(Throwable cause) {
		super(cause);
	}
	
	
}
